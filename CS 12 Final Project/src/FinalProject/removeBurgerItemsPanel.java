    private void saveRemoveBurgerItemsButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveRemoveBurgerItemsButtonMouseClicked
        frame = new JFrame("Exit");
        if (JOptionPane.showConfirmDialog(frame, "Are you sure you want to remove the selected item(s)?", "Save Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
        }
        // <editor-fold>//saveIngredients
        if (!bIng1Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.bingredients1.size(); i++) {
                if (FinalProject.bingredients1.get(i).equals(bIng1Box.getSelectedItem().toString())) {
                    FinalProject.bingredients1.remove(i);
                    break;
                }
            }
            addBurgerItemsPanel.saveBIng1();
        }
        if (!bIng2Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.bingredients2.size(); i++) {
                if (FinalProject.bingredients2.get(i).equals(bIng2Box.getSelectedItem().toString())) {
                    FinalProject.bingredients2.remove(i);
                    break;
                }
            }
            addBurgerItemsPanel.saveBIng2();
        }
        if (!bIng3Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.bingredients3.size(); i++) {
                if (FinalProject.bingredients3.get(i).equals(bIng3Box.getSelectedItem().toString())) {
                    FinalProject.bingredients3.remove(i);
                    break;
                }
            }
            addBurgerItemsPanel.saveBIng3();
        }
        if (!bIng4Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.bingredients4.size(); i++) {
                if (FinalProject.bingredients4.get(i).equals(bIng4Box.getSelectedItem().toString())) {
                    FinalProject.bingredients4.remove(i);
                    break;
                }
            }
            addBurgerItemsPanel.saveBIng4();
        }
        if (!sideBox.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.sideOrder.size(); i++) {
                if (FinalProject.sideOrder.get(i).equals(sideBox.getSelectedItem().toString())) {
                    FinalProject.sideOrder.remove(i);
                    break;
                }
            }
            addBurgerItemsPanel.saveSide();
        }
        if (!drinkBox.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.drinkOrder.size(); i++) {
                if (FinalProject.drinkOrder.get(i).equals(drinkBox.getSelectedItem().toString())) {
                    FinalProject.drinkOrder.remove(i);
                    break;
                }
            }
            addBurgerItemsPanel.saveDrink();
        }
        removeItems();
        loadItems();
        // </editor-fold>//ingredient arrays
    }//GEN-LAST:event_saveRemoveBurgerItemsButtonMouseClicked

