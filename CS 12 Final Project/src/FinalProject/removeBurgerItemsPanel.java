    public static void removeItems() {
        bIng1Box.removeAllItems();
        bIng2Box.removeAllItems();
        bIng3Box.removeAllItems();
        bIng4Box.removeAllItems();
        sideBox.removeAllItems();
        drinkBox.removeAllItems();
    }

    public static void loadItems() {
        // <editor-fold>//load Items       
        bIng1Box.addItem("N/A");
        bIng2Box.addItem("N/A");
        bIng3Box.addItem("N/A");
        bIng4Box.addItem("N/A");
        sideBox.addItem("N/A");
        drinkBox.addItem("N/A");
        for (int i = 0; i < FinalProject.bingredients1.size(); i++) {
            bIng1Box.addItem(FinalProject.bingredients1.get(i));
        }
        for (int i = 0; i < FinalProject.bingredients2.size(); i++) {
            bIng2Box.addItem(FinalProject.bingredients2.get(i));
        }
        for (int i = 0; i < FinalProject.bingredients3.size(); i++) {
            bIng3Box.addItem(FinalProject.bingredients3.get(i));
        }
        for (int i = 0; i < FinalProject.bingredients4.size(); i++) {
            bIng4Box.addItem(FinalProject.bingredients4.get(i));
        }
        for (int i = 0; i < FinalProject.sideOrder.size(); i++) {
            sideBox.addItem(FinalProject.sideOrder.get(i));
        }
        for (int i = 0; i < FinalProject.drinkOrder.size(); i++) {
            drinkBox.addItem(FinalProject.drinkOrder.get(i));
        }
        // </editor-fold>//end load Items
    }

