    public static void removeItems() {
        sIng1Box.removeAllItems();
        sIng2Box.removeAllItems();
        sIng3Box.removeAllItems();
        sIng4Box.removeAllItems();
        sideBox.removeAllItems();
        drinkBox.removeAllItems();
    }

    public static void loadItems() {
        // <editor-fold>//load Items       
        sIng1Box.addItem("N/A");
        sIng2Box.addItem("N/A");
        sIng3Box.addItem("N/A");
        sIng4Box.addItem("N/A");
        sideBox.addItem("N/A");
        drinkBox.addItem("N/A");
        for (int i = 0; i < FinalProject.singredients1.size(); i++) {
            sIng1Box.addItem(FinalProject.singredients1.get(i));
        }
        for (int i = 0; i < FinalProject.singredients2.size(); i++) {
            sIng2Box.addItem(FinalProject.singredients2.get(i));
        }
        for (int i = 0; i < FinalProject.singredients3.size(); i++) {
            sIng3Box.addItem(FinalProject.singredients3.get(i));
        }
        for (int i = 0; i < FinalProject.singredients4.size(); i++) {
            sIng4Box.addItem(FinalProject.singredients4.get(i));
        }
        for (int i = 0; i < FinalProject.sideOrder.size(); i++) {
            sideBox.addItem(FinalProject.sideOrder.get(i));
        }
        for (int i = 0; i < FinalProject.drinkOrder.size(); i++) {
            drinkBox.addItem(FinalProject.drinkOrder.get(i));
        }
        // </editor-fold>//end load Items
    }

