    private void saveRemoveSandwichItemsButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveRemoveSandwichItemsButtonMouseClicked
        frame = new JFrame("Exit");
        if (JOptionPane.showConfirmDialog(frame, "Are you sure you want to remove the selected item(s)?", "Save Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION) {
        }
        // <editor-fold>//saveIngredients
        if (!sIng1Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.singredients1.size(); i++) {
                if (FinalProject.singredients1.get(i).equals(sIng1Box.getSelectedItem().toString())) {
                    FinalProject.singredients1.remove(i);
                    break;
                }
            }
            addSandwichItemsPanel.saveSIng1();
        }
        if (!sIng2Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.singredients2.size(); i++) {
                if (FinalProject.singredients2.get(i).equals(sIng2Box.getSelectedItem().toString())) {
                    FinalProject.singredients2.remove(i);
                    break;
                }
            }
            addSandwichItemsPanel.saveSIng2();
        }
        if (!sIng3Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.singredients3.size(); i++) {
                if (FinalProject.singredients3.get(i).equals(sIng3Box.getSelectedItem().toString())) {
                    FinalProject.singredients3.remove(i);
                    break;
                }
            }
            addSandwichItemsPanel.saveSIng3();
        }
        if (!sIng4Box.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.singredients4.size(); i++) {
                if (FinalProject.singredients4.get(i).equals(sIng4Box.getSelectedItem().toString())) {
                    FinalProject.singredients4.remove(i);
                    break;
                }
            }
            addSandwichItemsPanel.saveSIng4();
        }
        if (!sideBox.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.sideOrder.size(); i++) {
                if (FinalProject.sideOrder.get(i).equals(sideBox.getSelectedItem().toString())) {
                    FinalProject.sideOrder.remove(i);
                    break;
                }
            }
            addSandwichItemsPanel.saveSide();
        }
        if (!drinkBox.getSelectedItem().toString().equals("N/A")) {
            for (int i = 0; i < FinalProject.drinkOrder.size(); i++) {
                if (FinalProject.drinkOrder.get(i).equals(drinkBox.getSelectedItem().toString())) {
                    FinalProject.drinkOrder.remove(i);
                    break;
                }
            }
            addSandwichItemsPanel.saveDrink();
        }
        removeItems();
        loadItems();
        // </editor-fold>//ingredient arrays
    }//GEN-LAST:event_saveRemoveSandwichItemsButtonMouseClicked

